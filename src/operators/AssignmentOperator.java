package operators;

public class AssignmentOperator {

	public static void main(String[] args) {
		int x = 5;
		System.out.println(x);
		
		int y = x;
		System.out.println(y);
		
		y += x;
		System.out.println(y);
		
		y -= x;
		System.out.println(y);
		
		y *= x;
		System.out.println(y);
		
		y /= x;
		System.out.println(y);
		
		y %= x;
		System.out.println(y);
		
		y &= x;
		System.out.println(y);

	}

}
