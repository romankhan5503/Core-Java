package operators;

public class ComparisonOperator {

	public static void main(String[] args) {
		int x = 9;
		
		
		if(x == 7) {
			System.out.println("no");
		}
		if(x != 7) {
			System.out.println("yes");
		}
		if(x > 7) {
			System.out.println("yes");
		}
		if(x < 7) {
			System.out.println("no");
		}
		if(x >= 7) {
			System.out.println("yes");
		}
		if(x <= 7) {
			System.out.println("no");
		}

	}

}
